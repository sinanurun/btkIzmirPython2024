#aritmetiksel operatörler sayılar arasında aritmetiksel
#işlemler yapmak için kullanılırlar
print(60 * 12) # * operatörü çarpma için kullanılır
print(60 / 12) # / operatörü bölme için kullanılır
print(60 % 9) # % operatörü mod almak için kullanılır (a%b) anın bye bölümünden kalanını verir
print(60 // 7) # // operatörütam bölme için kullanılır (a//b) anın bye bölümünden kalanını verir
print(60 - 7) # - operatörütam çıkartma işlemi için kullanılır
print(60 + 7) # + operatörütam toplama işlemi için kullanılır
print(60 ** 3) # ** operatörü üs alma için kullanılır (a**b) yani a üssü b


